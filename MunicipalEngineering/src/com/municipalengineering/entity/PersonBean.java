package com.municipalengineering.entity;

public class PersonBean {
	private String idCard;
	private String userName;
	private String cName;
	private String cardNum;
	private String cardXh;
	private String hasUseCard;
	public String getIdCard() {
		return idCard;
	}
	public void setIdCard(String idCard) {
		this.idCard = idCard;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getCName() {
		return cName;
	}
	public void setCName(String cName) {
		this.cName = cName;
	}
	public String getCardNum() {
		return cardNum;
	}
	public void setCardNum(String cardNum) {
		this.cardNum = cardNum;
	}
	public String getCardXh() {
		return cardXh;
	}
	public void setCardXh(String cardXh) {
		this.cardXh = cardXh;
	}
	public String getHasUseCard() {
		return hasUseCard;
	}
	public void setHasUseCard(String hasUseCard) {
		this.hasUseCard = hasUseCard;
	}
}
